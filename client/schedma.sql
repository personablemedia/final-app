CREATE TABLE VEHICLE (
    `VEHICLE_ID` INT PRIMARY KEY,
    `VEHICLE_YEAR` INT,
    `VEHICLE_MAKE` VARCHAR(255) CHARACTER SET utf8,
    `VEHICLE_MODEL` VARCHAR(255) CHARACTER SET utf8,
    `VEHICLE_TYPE_ID` INT,
    `VEHICLE_SHORT_DESC` VARCHAR(1600) CHARACTER SET utf8,
    `VEHICLE_DEALER_NOTES` VARCHAR(3200) CHARACTER SET utf8,
    `VEHICLE_MILES` INT,
    `VEHICLE_ENGINE` VARCHAR(255) CHARACTER SET utf8,
    `VEHICLE_VIN` VARCHAR(255) CHARACTER SET utf8,
    `VEHICLE_DEALER_ID` INT,
    `CPO` BOOLEAN,
    `VEHICLE_SALE_PRICE` INT,
    `VEHICLE_NADA_PRICE` INT
